CREATE TABLE CLIENTE
(
    COD_CLIENTE INT IDENTITY(1,1) PRIMARY KEY,
    NOMBRES VARCHAR(100) NOT NULL,
    APELLIDOS VARCHAR(100) NOT NULL,
    DNI CHAR(8) UNIQUE NOT NULL,
    DIRECCION VARCHAR(150) NOT NULL,
    TELEFONO VARCHAR(15) NOT NULL
);


CREATE TABLE CUENTA
(
    COD_CUENTA INT IDENTITY(1,1) PRIMARY KEY,
    COD_CLIENTE INT NOT NULL,
    NUMERO_CUENTA VARCHAR(20) UNIQUE NOT NULL,
    SALDO DECIMAL(10,2) NOT NULL,
    ESTADO VARCHAR(20) NOT NULL,
    CONSTRAINT FK_CUENTA_CLIENTE
        FOREIGN KEY (COD_CLIENTE)
        REFERENCES CLIENTE(COD_CLIENTE)
);


CREATE TABLE TRANSACCION
(
    COD_TRANSACCION INT IDENTITY(1,1) PRIMARY KEY,
    COD_CUENTA INT NOT NULL,
    TIPO VARCHAR(50) NOT NULL,
    MONTO DECIMAL(10,2) NOT NULL,
    FECHA DATE NOT NULL,
    CONSTRAINT FK_TRANSACCION_CUENTA
        FOREIGN KEY (COD_CUENTA)
        REFERENCES CUENTA(COD_CUENTA)
);


CREATE TABLE COMPROBANTE
(
    COD_COMPROBANTE INT IDENTITY(1,1) PRIMARY KEY,
    COD_TRANSACCION INT NOT NULL,
    TIPO VARCHAR(50) NOT NULL,
    FECHA DATE NOT NULL,
    TOTAL DECIMAL(10,2) NOT NULL,
    CONSTRAINT FK_COMPROBANTE_TRANSACCION
        FOREIGN KEY (COD_TRANSACCION)
        REFERENCES TRANSACCION(COD_TRANSACCION)
);


CREATE TABLE AUDITORIA
(
    COD_AUDITORIA INT IDENTITY(1,1) PRIMARY KEY,
    COD_CLIENTE INT NOT NULL,
    COD_CUENTA INT NOT NULL,
    COD_TRANSACCION INT NOT NULL,
    COD_COMPROBANTE INT NOT NULL,
    USUARIO_SISTEMA VARCHAR(100) NOT NULL,
    ACCION_REALIZADA VARCHAR(150) NOT NULL,
    FECHA_REGISTRO DATETIME NOT NULL DEFAULT GETDATE(),

    CONSTRAINT FK_AUDITORIA_CLIENTE
        FOREIGN KEY (COD_CLIENTE) REFERENCES CLIENTE(COD_CLIENTE),

    CONSTRAINT FK_AUDITORIA_CUENTA
        FOREIGN KEY (COD_CUENTA) REFERENCES CUENTA(COD_CUENTA),

    CONSTRAINT FK_AUDITORIA_TRANSACCION
        FOREIGN KEY (COD_TRANSACCION) REFERENCES TRANSACCION(COD_TRANSACCION),

    CONSTRAINT FK_AUDITORIA_COMPROBANTE
        FOREIGN KEY (COD_COMPROBANTE) REFERENCES COMPROBANTE(COD_COMPROBANTE)
);


