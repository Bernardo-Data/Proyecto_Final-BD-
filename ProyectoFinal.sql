--5.CONSULTAS UNO A MUCHOS

--a. REALIZAR CONSULTAS POR CROSS JOIN --- 2 CONSULTAS

SELECT CL.NOMBRES, CL.APELLIDOS, T.TIPO FROM CLIENTE CL CROSS JOIN TRANSACCION T
SELECT CU.NUMERO_CUENTA, CU.SALDO, CO.TIPO FROM CUENTA CU CROSS JOIN COMPROBANTE CO


--b. REALIZAR CONSULTAS POR INNER JOIN --- 2 CONSULTAS

SELECT CL.NOMBRES, CL.APELLIDOS, CU.NUMERO_CUENTA FROM CLIENTE CL INNER JOIN CUENTA CU ON CL.COD_CLIENTE = CU.COD_CLIENTE
SELECT T.TIPO, T.MONTO, A.USUARIO_SISTEMA FROM TRANSACCION T INNER JOIN AUDITORIA A ON T.COD_TRANSACCION = A.COD_TRANSACCION


--c. REALIZAR CONSULTAS POR LEFT JOIN --- 2 CONSULTAS

SELECT CL.NOMBRES, CL.APELLIDOS, A.ACCION_REALIZADA FROM CLIENTE CL LEFT JOIN AUDITORIA A ON CL.COD_CLIENTE = A.COD_CLIENTE
SELECT CU.NUMERO_CUENTA, CU.SALDO, T.TIPO FROM CUENTA CU LEFT JOIN TRANSACCION T ON CU.COD_CUENTA = T.COD_CUENTA


--d. REALIZAR CONSULTAS POR RIGHT JOIN --- 2 CONSULTAS

SELECT CO.TIPO, CO.TOTAL, T.TIPO FROM TRANSACCION T RIGHT JOIN COMPROBANTE CO ON T.COD_TRANSACCION = CO.COD_TRANSACCION
SELECT CL.NOMBRES, CL.APELLIDOS, CU.ESTADO FROM CLIENTE CL RIGHT JOIN CUENTA CU ON CL.COD_CLIENTE = CU.COD_CLIENTE


